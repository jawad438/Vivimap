function parseLegalText(text: string) {
    const sections = text.trim().split(/\n(?=VIVIMAP (TERMS AND CONDITIONS|USAGE POLICY|PRIVACY POLICY))/);
    
    return sections.map(sectionText => {
        const lines = sectionText.trim().split('\n');
        const title = lines[0];
        const lastUpdated = lines[1];
        let content = lines.slice(3).join('\n');

        // Convert numbered lists to HTML
        content = content.replace(/^\d+\. (.*)$/gm, '<h3>$1</h3>');
        
        // Convert sub-lists (like in section 3 of Terms) to HTML
        content = content.replace(/^(Users must.*)$/gm, '<p>$1</p>');
        content = content.replace(/^(Sexually explicit content|Pedophilic or grooming-related content|Graphic depictions of violence or gore|Unauthorized advertising or promotional content|Harassment or threatening behavior)$/gm, '<li>$1</li>');
        content = content.replace(/(<li>.*<\/li>)/gs, '<ul>$1</ul>');
        
        // Convert other sub-lists
        content = content.replace(/^(A formal warning.*|Temporary suspension.*|Permanent account termination.*)$/gm, '<li>$1</li>');
        content = content.replace(/^(With your consent|To comply with legal.*|To protect the rights.*|In connection with a merger.*)$/gm, '<li>$1</li>');
        content = content.replace(/^(To operate, maintain.*|To manage user accounts.*|To enforce the Platform’s.*|To detect, prevent.*|To communicate with Users.*)$/gm, '<li>$1</li>');
        content = content.replace(/^(Contact Information.*|Technical Information.*|Geolocation Data.*|Usage Data.*)$/gm, '<li>$1</li>');

        
        // Wrap remaining paragraphs
        content = content.split('\n\n').map(p => {
             if (p.startsWith('<h3>') || p.startsWith('<ul>') || p.startsWith('<li>') || p.startsWith('<p>')) return p;
             return `<p>${p}</p>`;
        }).join('');

        return {
            title,
            lastUpdated,
            content
        };
    });
}

